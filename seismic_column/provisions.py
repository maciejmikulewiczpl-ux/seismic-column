# Backwards-compatible alias: older code/tests referenced ``SDC_2_0``.
SDC_2_0 = SDC_2_1

PROVISIONS: dict[str, CodeProvisions] = {
    SDC_2_1.key: SDC_2_1,
    AASHTO_SGS_3.key: AASHTO_SGS_3,
}

# Older saved projects may carry the previous "SDC 2.0" key.
_KEY_ALIASES = {"SDC 2.0": "SDC 2.1"}


def get_provisions(key: str) -> CodeProvisions:
    """Return the provisions for ``key`` (defaults to Caltrans SDC 2.1)."""
    return PROVISIONS.get(_KEY_ALIASES.get(key, key), SDC_2_1)
